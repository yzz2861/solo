export const getProblemById = (id: string): Problem | undefined => {
  return problems.find(p => p.id === id);
};

export const getProblemsByType = (type: Problem['type']): Problem[] => {
  return problems.filter(p => p.type === type);
};

export const getProblemsByDifficulty = (difficulty: 1 | 2 | 3): Problem[] => {
  return problems.filter(p => p.difficulty === difficulty);
};

export const addCustomProblem = (problem: Problem): void => {
  problems.push(problem);
};
